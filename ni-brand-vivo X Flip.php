              <p>Is it foldables season at GSMArena or what? It sure feels like it is raining phones that bend in half left and right. We have here the X Flip, vivo's first attempt at a clamshell-style handset. No stranger to bendy phones, the company is at gen two of the phone-turns-tablet design, but the X Flip only now joins the likes of the Galaxy Z Flip and Motorola Razr to battle for the hearts of smaller phone lovers.
                </p>
