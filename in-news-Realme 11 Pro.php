                        <h2 class="text-center">News Description</h2>
                        <p>The Realme 11 Pro comes in a yellow-colored retail package like the Pro+ model, and its in-box contents are also
                             identical, except you get an 80W SuperVOOC charger with the Realme 11 Pro instead of the 100W GaN charger that
                              comes bundled with the Pro+. Realme didn't put pictures of its new brand ambassador Shah Rukh Khan for the smartphones
                               category on the retail package as it did years ago with
                             Salman Khan, who was the brand ambassador for the Realme 6 series in India. Not everyone likes that after all.
                        </p>
                        <div class="swiper-slide">
                            <img src="assets/img/portfolio/7.1.png" alt="">
                        </div>
                        </div>
                        <p>The Realme 11 Pro is built around a 6.7" 120Hz FullHD+ AMOLED display with HDR10+ support and 950 nits peak brightness.
                             The screen is protected by Dragontrail Glass and has a centered punch hole for the
                             16MP selfie camera (Pro+ features a 32MP unit) with a fingerprint reader underneath for biometric authentication.</p>
                        <p>Turn the phone around, and you see a massive, circular camera module, home to LED flash and two cameras - 100MP primary (with OIS) and 2MP macro. Realme ditched the 8MP ultrawide unit from the Pro+ model, which doesn't sit well with us. The brand could've opted for an ultrawide unit with autofocus to allow
                             macro shots if it really wanted to have two rear cameras on the Pro version, but we believe Realme went with this arrangement as a cost-cutting measure.</p>
                    <p>The Realme 11 Pro, like the Pro+, has three color options - Astral Black,
                         Sunrise Beige, and Oasis Green. We received the Sunrise Beige model, which,
                          like the Oasis Green variant, has a back cover flaunting the Lychee Leather 
                          Design, which not only makes it look premium and expensive but also helps with 
                          the grip while making the rear panel resistant to fingerprint smudges.</p>
                          <div class="swiper-slide">
                            <img src="assets/img/portfolio/7.2.png" alt="">
                        </div>
                        </div>
                    <p>The leather version feels great to hold in hand. However, it's not the first time Realme launched a smartphone with a leather back. In 2020, Realme released the Realme 7 Pro SE Sun Kissed Leather, which had a back cover with a similar feel, with the Realme logo having a metallic effect as it does on the Realme 11 Pro and Pro+.</p>
                    <p>The Realme 11 Pro looks identical to the Realme 11 Pro+, and you may not be able to tell them apart at first glance. That doesn't mean the two phones can't be differentiated. The Pro model has "100MP OIS Camera" written on its camera module, while the Pro+ has "200MP OIS Camera" text. Besides, both smartphones have different rear camera setups.</p>
                    <p>Under the hood, the Realme 11 Pro has the Dimensity 7050 SoC with up to 12GB RAM and 256GB storage. It packs a 5,000 mAh battery with 67W charging support and runs Android 13-based Realme UI 4.0 out of the box. Realme has promised two years of Android version upgrades and three years of security updates for the Pro and Pro+. It's also worth mentioning that the Realme 11 Pro and Pro+ come pre-installed with several third-party apps, but fortunately, most of them can be uninstalled if you wish to.</p>
                    <p>We will start testing the Realme 11 Pro soon. In the meantime, you can read our Realme 11 Pro+ review. It's basically the Realme 11 Pro with a 200MP primary camera, an 8MP ultrawide camera, a 32MP selfie shooter, and 100W charging.</p>
